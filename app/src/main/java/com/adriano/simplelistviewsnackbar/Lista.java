package com.adriano.simplelistviewsnackbar;

public class Lista {
	String planeta;
	int imagem;
	int id;

	public void setId(int id) {
		this.id = id;
	}
	public int getId() {
		return id;
	}

	public void setImagem(int imagem) {
		this.imagem = imagem;
	}
	public int getImagem() {
		return imagem;
	}

	public void setPlaneta(String planeta) {
		this.planeta = planeta;
	}
	public String getPlaneta() {
		return planeta;
	}
}
